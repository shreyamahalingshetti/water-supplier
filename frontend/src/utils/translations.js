/** Helper — returns a translation string, supports {placeholder} substitution */
export function t(translations_obj, key, vars = {}) {
  let str = translations_obj[key] || key;
  Object.entries(vars).forEach(([k, v]) => {
    str = str.replace(`{${k}}`, v);
  });
  return str;
}
